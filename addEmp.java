    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField1.getText().isEmpty() || jTextField3.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "الرجاء تعبئة جميع الحقول");
        }
        String name = jTextField1.getText().trim();
        String surname = jTextField3.getText().trim();
        int jobindx = jComboBox1.getSelectedIndex();
        Job job = jobs.get(jobindx);
        Emp employee = new Emp(name, surname, job, prp[0], prp[1], prp[2]);

        for (int i = 0; i < emp.size(); i++) {
            if (name.equals(emp.get(i).getName()) && surname.equals(emp.get(i).getSurname())) {
                JOptionPane.showMessageDialog(null, "موجود بالفعل");
                break;
            }
            if (i == emp.size() - 1 && name != emp.get(i).getName() && surname != emp.get(i).getSurname()) {
                emp.add(employee);
                saveData();
                break;
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

