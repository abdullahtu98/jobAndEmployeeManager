    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String[] jobsarray = new String[jobs.size()];
        if (jCheckBox1.isSelected()) {
            prp[0] = true;
        }
        if (jCheckBox2.isSelected()) {
            prp[1] = true;
        }
        if (jCheckBox3.isSelected()) {
            prp[2] = true;
        }
        for (int i = 0; i < jobs.size(); i++) {
            if (jobs.get(i).isLang() == prp[0] && prp[1] == jobs.get(i).isMath() && prp[2] == jobs.get(i).isCs() || (prp[1] == true && prp[0] == true && prp[2] == true)) {
                jobsarray[i] = jobs.get(i).getJob() + ", $" + formatter.format(jobs.get(i).getSalary());
            } else {
                jobsarray[i] = "غير مطايق للمؤهلات";
            }
        }
        jComboBox1.setModel(new javax.swing.DefaultComboBoxModel<>(jobsarray));
        prp[0] = false;
        prp[1] = false;
        prp[2] = false;
    }//GEN-LAST:event_jButton2ActionPerformed

